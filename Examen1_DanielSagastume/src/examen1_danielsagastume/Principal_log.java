    private void jTabbedPane1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabbedPane1StateChanged
        if (jTabbedPane1.getSelectedIndex() == 1) {
            for (Object t : users) {
                usuarios.addItem(((Users) t).getUser());
            }
        }
    }//GEN-LAST:event_jTabbedPane1StateChanged

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        //Logging
        if (users.get(usuarios.getSelectedIndex()).getContrasenia().
                equals(log_contrasenia.getText())) {
            this.setVisible(false);
            Users usuario=users.get(usuarios.getSelectedIndex());
            User_pantalla u=new User_pantalla();
            u.setVisible(true);
            u.setUsuario(usuario, libros);
            pos=usuarios.getSelectedIndex();
        } else {
            JOptionPane.showMessageDialog(this, "ContraseÃ±a incorrecta");
            log_contrasenia.setText("");
        }
    }//GEN-LAST:event_jButton2MouseClicked

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        users.add(new Users(user.getText(), contrasenia.getText(), Integer.parseInt(codigo.getText()),nombre.getText(), apellido.getText(), Integer.parseInt(edad.getText())));
        JOptionPane.showMessageDialog(this, "Usuario agregado");
        user.setText("");
        contrasenia.setText("");
        codigo.setText("");
        nombre.setText("");
        apellido.setText("");
        edad.setText("");
    }//GEN-LAST:event_jButton1MouseClicked

